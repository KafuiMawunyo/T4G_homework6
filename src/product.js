//dropdown for watermelon
function dropdownWatermelon() {
    let watermelon = document.getElementById("watermelon");
    watermelon.style.display = "block";
  }
  
  let watermelonH = document.getElementById("watermelonH");
  watermelonH.addEventListener("click", dropdownWatermelon); 

